																				 Digital Crisis offers a new premium backup service in addition to their 
                                                                                 computer repair services. The service entails a program installed locally 
                                                                                 on the user’s computer and the program remotely backs up the data to multiple 
                                                                                 data centers throughout the U.S., providing redundancy and fail over 
                                                                                 protection. Digital Crisis is also offering a free backup service for 
                                                                                 QuickBooks, Peachtree users to back up their accounting files. “Backing up 
                                                                                 your data is extremely crucial in the business world especially your 
                                                                                 accounting files, you don’t want to know what it’s like to open up your 
                                                                                 QuickBooks program and it say “File not found.” and not have a backup to 
                                                                                 recover from,” says Zachary Kitchen, Vice President of Digital Crisis. 
                                                                                 Digital Crisis highly recommends everyone with a computer to back up their 
                                                                                 valuable data, as data recovery can be very expensive , “It’s not a matter if 
                                                                                 a hard drive fails, it’s just a matter of when,” says Billy Hathcock, CEO of 
                                                                                 Digital Crisis.
