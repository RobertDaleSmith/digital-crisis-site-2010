                                      <td align="left" valign="top" class="style91" style="height:25px;">What is the difference between data recovery and data back-up?</td>
									</tr>
                                    <tr>
                                      <td align="left" valign="top" class="style6">
										Data Recovery is a service we offer to recover lost or accidentally deleted files. We can also recover files from corrupted and failing hard drives. Recovering files is a long process that requires the use of multiple specialized software scanners to scan the entire drive. Once data is recovered, a technician can transfer the files to another computer or to a new hard drive.<br />
                                        <br />
                                        Data Backup is a set of services we offer to prevent the need for data recovery. Our services include automatic local file backup solutions, as well as online file backup solutions. All electronics are prone to failure. Some hard drives last longer then others and some are simply faulty. So it is always very important to have multiple layers of backup setup on a schedule. Call today and ask a technician about <em>the three layers of backup</em>..</td>
